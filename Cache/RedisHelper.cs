        /// <summary>
        /// 只做一次系统配置初始化
        /// </summary>
        /// <param name="redisConnect">初始化Redis连接字符串</param>
        /// <param name="sysCustomPrefix">初始化Key名前缀</param>
        public static void InitConfig(string redisConnect, string sysCustomPrefix = "DefaultPrefix:")
        {
            initKeyPrefix = sysCustomPrefix;
            RedisConnectionHelp.InitConfig(redisConnect);
            RedisConnectionHelp.GetConnectionMultiplexer(redisConnect);
        }

        /// <summary>
        /// 按系统初始化InitConfig配置，实例化Redis帮助工具
        /// </summary>
        public RedisHelper()
        {
            CustomKey = initKeyPrefix;
            _conn = RedisConnectionHelp.Instance;
        }

        /// <summary>
        /// 实例化Redis帮助工具
        /// </summary>
        /// <param name="redisConnect">Redis连接字符串</param>
        /// <param name="sysCustomPrefix">Key名前缀，默认DefaultPrefix:值</param>
        /// <param name="dbNum">db连接数</param>
        public RedisHelper(string redisConnect, string sysCustomPrefix = "DefaultPrefix:", int dbNum = 0)
                : this(redisConnect, dbNum)
        {
            CustomKey = sysCustomPrefix;
        }

        /// <summary>
        /// 实例化Redis帮助工具
        /// </summary>
        /// <param name="readWriteHosts">连接池的新连接字符串；Key名前缀默认DefaultPrefix:值</param>
        /// <param name="dbNum">db连接数</param>
        public RedisHelper(string readWriteHosts, int dbNum)
        {
            DbNum = dbNum;
            RedisConnectionHelp.InitConfig(readWriteHosts);
            _conn =
            string.IsNullOrWhiteSpace(readWriteHosts) ?
            RedisConnectionHelp.Instance : RedisConnectionHelp.GetConnectionMultiplexer(readWriteHosts);
        }

